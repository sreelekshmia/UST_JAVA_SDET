package com.ust.test;

public class ArrayForExample {
	public static void main(String[] args) {
		//create array
		int[] age = {19,18,5,10};
		//display the array using for loop
		System.out.println("Using for loop :");
		for(int i=0;i<age.length;i++)
		{
			System.out.println(age[i]);
		}
	}

}
